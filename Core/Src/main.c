  setvbuf(stdout, NULL, _IONBF, 0);
  printf("\r\n=== turret STM32 boot (proto v%u) ===\r\n", PROTO_VERSION);

  uart_rpi_init(&huart1);                  // USART1(RPi 링크) 수신 시작
  lidar_init(&huart6);                     // 📡 USART6(라이다 센서) 드라이버 기동 및 인터럽트 시작
